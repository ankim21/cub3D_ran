#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			i++;
		}
		write(fd, s, i);
	}
	write(fd, "\n", 1);
}
