#include "libft.h"

int	ft_atoi(char *s)
{
	long long	i;
	int			res;
	int			sign;

	res = 0;
	i = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\v' \
		|| s[i] == '\n' || s[i] == '\r' || s[i] == '\f')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		if ((res * 10 + (s[i] - '0')) > INT_MAX)
			return (-1);
		res = res * 10 + (s[i] - '0');
		i++;
	}
	return (res * sign);
}
